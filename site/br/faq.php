					<h3>Minhas imagens</h3>
"Minhas imagens" é uma área em nosso site onde você poderá fazer e salvar sua seleção pessoal. Essa seção foi criada para que você tenha sua própria área de trabalho dentro do site.<br/>
Abaixo da foto ampliada existe um link "Adicionar" que encaminhará a imagem para uma pasta criada e nomeada por você.<br/>
Essa seção permite o envio de pastas por e-mail, alteração de dados em seu cadastro, exclusão de imagens e muito mais. Aqui você verá todas as imagens que separou, poderá concluir sua seleção e cotar sua edição.
<br/><br/>
Para utilizar essa seção você precisa ser cadastrado.<br/>
Caso ainda não seja, você deverá fazer seu cadastro na respectiva seção.<br/>
Se você perdeu sua senha clique na opção “Esqueceu sua senha?”, digite o email cadastrado e ela será enviada.
<br/><br/>
- Criar Nova<br/>
Clicando aqui você poderá criar uma nova pasta para reunir suas fotos. Basta atribuir um nome para essa pasta e clicar em “Criar Pasta”. Automaticamente sua pasta estará pronta para receber as imagens selecionadas por você.
<br/><br/>
- Renomear<br/>
Essa ferramenta foi criada para que você possa atribuir um novo nome a uma pasta já existente.<br/>
Basta selecionar a pasta que deseja renomear, clicar no botão e então digitar o novo nome escolhido. Clique finalmente no botão “Renomear” e sua pasta aparecerá com seu novo nome. Seu conteúdo permanecerá inalterado.
<br/><br/>
- Mesclar<br/>
Caso você precise mesclar duas ou mais pastas no transcorrer do seu trabalho, selecione as pastas que deseja reunir e clique em “Mesclar”. Dê o novo nome que deseja à essa fusão e confirme clicando em Mesclar no pop onde digitou o nome. A nova pasta estará criada reunindo o conteúdo das pastas que haviam sido selecionadas. Essas, porém, permanecerão na relação de pastas aguardando sua ordem de exclusão. Isso permite que você reúna pesquisas diversas de várias formas, sem perder a sua pesquisa inicial.<br/>
Essa ferramenta também serve para a duplicação de uma pasta. Basta selecionar a pasta que deseja duplicar e clicar no botão “Mesclar”, atribua nome à nova pasta e confirme clicando novamente em “Mesclar”.
<br/><br/>
- Excluir<br/>
Para excluir uma pasta basta selecioná-la e clicar em “Excluir”. Ela será automaticamente removida da sua relação de pastas.
<br/><br/>
- Cotar<br/>
Clique na foto desejada e selecione a opção “Cotar”. Confirme o pedido. A foto será encaminhada à área de cotação. Maiores explicações você encontrará na seção específica “Cotação”.
<br/><br/>
- Enviar pasta por e-mail<br/>
Essa ferramenta permite que você envie uma pasta para uma outra pessoa. Selecione a pasta que deseja enviar clicando no quadrado que se encontra à esquerda do "Nome da Pasta". Clique então em "Enviar pasta por e-mail". Surgirá uma tela onde você deverá preencher o seu nome e e-mail, para efeito de identificação. A seguir digite o e-mail da pessoa para quem deseja enviar a pasta. Preencha o campo “Com cópia para” caso deseje enviá-la a uma terceira pessoa. Se quiser acrescentar algum comentário à sua seleção ou apenas ressaltar alguma observação preencha o campo “Comentários”. Ao final clique em enviar.
				</li>

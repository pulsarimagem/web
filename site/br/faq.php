Na área de Cotação, ao clicar em “Cotar” surgirá uma tela onde você deverá fazer uma descrição do uso que dará para a(s) foto(s) escolhida(s). Quanto mais específica for a sua descrição mais objetiva será a atribuição do valor a ser cobrado pela(s) foto(s). Finalmente especifique se a distribuição do produto onde a(s) foto(s) será(ão) utilizada(s) será Nacional ou internacional. 

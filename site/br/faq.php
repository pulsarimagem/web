			<ul>
				<li>
					<h3> A conta no Pulsar é paga?</h3>
					Não. A criação de uma conta no site é gratuita. Através dela é possível realizar cotações e armazenar suas fotos prediletas.
				</li>
				<li>
					<h3>Por que se cadastrar no site?</h3>
					Ao se cadastrar, você poderá utilizar as diversas ferramentas que o site dispõe para facilitar o seu trabalho.
				</li>
				<li>
					<h3>Como faço para me cadastrar?</h3>
					Selecione a opção Cadastro no menu e preencha todos os campos observando apenas se você deseja se cadastrar como Pessoa Física ou Jurídica.
				</li>
				
				<li>
					<h3>Busca</h3>
Você poderá iniciar a sua pesquisa de duas formas:
<br/><br/>
- Palavra-Chave<br/>
digite aqui a palavra-chave (ou descritor) que deseja pesquisar e confirme. O retorno de sua busca logo aparecerá, mencionando o termo pesquisado e o número de imagens encontradas. Você poderá escolher o número de fotos que deseja mostrar em cada tela e ver sua pesquisa através dos thumbnails ou das imagens ampliadas. Neste caso você poderá ler todas as identificações pertencentes a cada uma delas.<br/>
- Código<br/>
caso você saiba o código de identificação da imagem que deseja visualizar basta digitá-lo no campo correspondente e confirmar. O retorno será imediato. Se você digitou o código errado o sistema emitirá uma mensagem de erro informando. 
				</li>
				<li>
					<h3>Temas</h3>
Criamos uma relação de Temas para facilitar sua busca. Basta percorrê-la com o mouse e os assuntos aparecerão. Se você se interessar por um deles clique e as fotos relacionadas aparecerão.
<br/><br/>
Sempre que você escolher o termo mais genérico terá como retorno todas as imagens que possam ter relação com o assunto selecionado. Porém se selecionar alguma subdivisão poderá visualizar as imagens mais específicas.<br/>
Por exemplo: se você clicar em Natureza / Parques Nacionais verá todas fotos relacionadas aos Parques Nacionais disponíveis no site. Mas se escolher Natureza / Parques Nacionais / Parque Nacional Das Emas (GO) verá apenas as fotos relacionadas a esse Parque. 
				</li>
				<li>
					<h3>Pesquisa Avançada</h3>
Essa seção permite que você faça suas pesquisas de forma mais pontual. Disponibilizamos diversas ferramentas de busca que, combinadas entre si, poderão retornar as fotos mais específicas.<br/>
Usando a opção “E” você obtém a soma de um ou mais termos. Por exemplo:<br/>
Soja “E” Colheita – retornarão fotos apenas da colheita da soja. Não virão todas as fotos de soja nem todas as fotos de colheita.<br/>
Soja “Não Contenha a Palavra” Colheita – retornarão todas as fotos de soja disponíveis, exceto as de colheita da soja.<br/>
Poderá ser feita uma combinação de assunto e autor de forma a visualizar somente as fotos que determinado autor produziu sobre o assunto.<br/>
Escolhendo apenas o nome do autor, sem determinar o assunto, você poderá visualizar todas as fotos desse autor que estão disponíveis no site.<br/>
Pode-se também determinar a orientação das fotos, caso você deseje apenas Horizontal ou Vertical e, clicando nos dois campos, virão ambas as orientações.<br/>
Todos esses campos poderão ser trabalhados isoladamente ou em conjunto. A elaboração de sua estratégia de pesquisa definirá a consistência de seu retorno. 
				</li>
                <li>
                <h3>Pesquisa por Estado</h3>
Dentro da Pesquisa Avançada criamos um mapa do Brasil dividido em estados. Cada área é destacada conforme o mouse pousa sobre a mesma. Ao clicar sobre o estado todos os temas que são relativos aquela região são mostrados possibilitando uma filtragem na busca de acordo com a região de interesse.

                </li>
		  <li>
					<h3>Retorno de Pesquisa</h3>
Ao fazer uma pesquisa a página seguinte trará o número de fotos encontradas sobre o assunto. No canto superior direito, você poderá escolher o número de fotos que deseja exibir em sua tela.
Ao rolar o mouse sobre a imagem você terá acesso as informações básicas da foto. Passando o mouse sobre a lupa e possível visualizar uma versão maior da imagem. Para adicionar imagens a Minhas Imagens, basta clicar sobre o ícone de lâmpada e selecionar a pasta em que deseja adicionar a imagem.<br /> Clicando sobre a imagem ela será ampliada com as principais informações de identificação desta imagem. Ao pé da página de retorno existe uma seqüência numérica referente ao número de páginas que contém o resultado de sua pesquisa. O número que aparecer em negrito refere-se à página que você está visualizando. Clique sobre o número seguinte ou apenas na opção “próxima página” para continuar visualizando sua pesquisa.				</li>
				<li>
					<h3>Ampliação de Imagem</h3>
Quando você estiver visualizando uma foto ampliada, verá também as principais informações de identificação desta imagem. As informações básicas de cada imagem são: código de identificação da foto, assunto principal, autor da foto, local da foto (cidade, Estado, ou país), data de realização da foto (dia/ mês/ano), autor da foto e as dimensões do arquivo em alta resolução.<br/>
As outras informações são interativas. O campo “Temas” são links que poderão te remeter a novas pesquisas dentro do site. “Palavras-chave ” são Tags que podem ser adicionadas a busca possibilitando um cruzamento desses termos para gerar uma nova pesquisa.<br/>
Existem também outras ferramentas de trabalho:
<br/><br/>
- Salvar<br/>
Você poderá salvar a imagem selecionada, em baixa resolução, em seu microcomputador. O uso dessa imagem, porém, deverá sempre obedecer às leis de Direitos Autorais (copyright) mencionadas abaixo.
<br/><br/>
- Adicionar às Minhas imagens<br/>
Clicando nesse botão você enviará essa imagem a uma área em nosso site onde você poderá fazer e salvar sua seleção pessoal.<br/>
Clique aqui para saber mais sobre a área “Minhas imagens”
<br/><br/>
- Enviar por e-mail<br/>
Você poderá enviar esta foto para um ou mais e-mail(s) que desejar. Preencha o campo com seu nome e endereço de e-mail para que sirva como identificação de remetente. Se quiser inserir alguma observação para acompanhar a foto, escreva no campo “Comentários”. O destinatário de sua mensagem poderá ler seu texto e visualizar a imagem que você enviou.
<br/><br/>
- Cotar<br/>
Clicando nesse botão você poderá proceder à cotação de preço da imagem selecionada. Clique aqui para saber mais sobre a utilização da área de “Cotação.
<br/><br/>
- Download<br/>
Essa funcionalidade permite que você baixe a imagem em média ou alta resolução diretamente do site. Para poder utilizar essa ferramenta você deverá efetuar seu cadastro “Pessoa Jurídica”. Com base nas informações fornecidas nesse cadastro a Pulsar irá liberar seu acesso, determinando o número de fotos por dia que sua empresa poderá baixar para arquivos em alta. Para arquivos de layout o uso será liberado. É importante esclarecer que todas as fotos em alta resolução baixadas , não sendo solicitadas como layout, serão cobradas. 
Uma vez autorizado seu acesso o procedimento a seguir será:
<br />
1) fazer a pesquisa utilizando o código da foto ou palavra-chave,
<br />
2) clicar sobre a foto escolhida,
<br />
3) na barra inferior da foto ampliada, clicar sobre o botão DOWNLOAD. Certifique-se que você esta devidamente logado,<br />
4) Selecionar a opção desejada: Download ou Layout,<br />
5) Preencher os campos solicitados. <br />
<strong>Para imagens de Download em alta resolução você deve clicar em “ACEITO”. Nesse momento você confirmará seu interesse no download, assumindo seu compromisso de compra,</strong> <br />
6) Salvar a foto em seu computador. 

<br/><br/>
- Avançar / Voltar<br/>
Há ainda as opções de avançar e / ou voltar as imagens de sua pesquisa. Clicando sobre um destes ícones você verá a imagem também ampliada que está imediatamente anterior ou posterior àquela que você está visualizando, podendo ir dessa forma até o começo ou final de sua pesquisa. 

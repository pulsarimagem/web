					<h3>Advanced Search</h3>
This section allows you to make more refined searches. We make available several search tools that combined can lead to more specific results. <br />
  Using the “and” option, you can make a search by the addition of 2 or more terms. For example: soy “and” harvest – the results will show only pictures of soy harvesting, and no pictures of general harvesting or soy beans. <br />
  If you choose, for example, the “does not contain the word” option, you can search like soy “does not contain the word” harvest. And the results will show you all soy pictures, but none of soy harvesting. <br />
You can also combine subject and author, so you can see only the pictures of one author about that subject.<br />
If you choose only the author, you will see all pictures from that photographer.<br />
You can also search by picture orientation, vertical or horizontal.<br />
You can use all the fields by themselves or combined – the way you use the fields will determine the results of the search. 

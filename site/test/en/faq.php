The “my images” section allows you to see all your selections and quote them, send folders by e-mail, modify your register data, delete images and more.<br />
To access “My Images” you need to be a registered user.<br /><br />
- Create new <br />
Clicking here you can create a new folder to save your images. Just name it and click on create folder, and your folder will be ready to receive pictures selected by you. <br /><br />
- Rename<br />
Use this to rename an existing folder. Simply select the folder you wish to rename, click on the button “rename” and type in the new name. Click on “rename” again and your folder will show up with the new name. Its content will remain the same. <br /><br />
- Merge<br />
  In case you need to unite two or more folders, select the folders and click “merge”. Give a name to this new folder and confirm, clicking on “merge” at the pop-up. The new folder created will contain the images of the folders you selected to mix. The original folders will still appear on your list, so you can organize your images in several different ways without losing your original search. <br />
You can also use this tool to duplicate a folder. Just select the folder you need to duplicate, click on “merge” and give it a new name. Finally, click on “merge” to confirm. <br /><br />
 - Delete<br />
